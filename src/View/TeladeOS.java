    private void pequisarOs(){

        
        String NumOs = JOptionPane.showInputDialog("Numero da Ordem de Serviço");
        String sql = "select * from ordemservicos where id = "+NumOs;
        try{
                           // Restriçao de Botoes e Telas
              btnSalvar.setEnabled(false);
              btnEditar.setEnabled(true);
              btnDelete.setEnabled(true);
              btnCancelar.setEnabled(true);
              //txtTelaOsPesquisaNomeCliente.setEnabled(false);
              //jTTelaOSPesquisaInfoCli.setVisible(false);
              
            pst=conexao.prepareStatement(sql);
            rs=pst.executeQuery();
            if (rs.next()) {
                txtTelaOSNumOS.setText(rs.getString(1));
                txtTelaOsData.setText(rs.getString(2));
                jcbTelaOsGarantia.setSelectedItem(rs.getString(3));
                jtxDescOS.setText(rs.getString(4));
                txtTelaOsDefeito.setText(rs.getString(5));
                jcbTelaOsStatus.setSelectedItem(rs.getString(6));
                txtTelaOsServico.setText(rs.getString(7));
                txtValorOS.setText(rs.getString(8));
                txtTelaOSPesquisaIdCli.setText(rs.getString(9));
                txtTelaOsTec.setText(rs.getString(10));
                txtBuscarProdutId.setText(rs.getString(11));
                txtDescricao.setText(rs.getString(12));
                txtNserie.setText(rs.getString(13));
                txtModelo.setText(rs.getString(14));
                txtFabricante.setText(rs.getString(15));
                                       
                // Setar Buton
                String btTipo=rs.getString(16);
                if (btTipo.equals("Ordem de Servico")){
                    jrbTelaOSOS.setSelected(true);
                    tipo="Ordem de Servico";
                    
                }else {
                    jrbTelaOsOracamento.setSelected(true);
                    tipo="Orcamento";
                }
        
                               
            } else {
 
                JOptionPane.showMessageDialog(null, "Ordem de Serviço não Cadastrada");
            }
            
        }catch (com.mysql.jdbc.exceptions.jdbc4.MySQLSyntaxErrorException e) {
            JOptionPane.showMessageDialog(null,"Ordem de Serviço Inválida");
            //System.out.println(e); 
        }catch (Exception e2){
            JOptionPane.showMessageDialog(null,e2);
        }
        
    }

