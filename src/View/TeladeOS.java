    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // TODO add your handling code here:
        Os emitir = new Os();
        
        //Validaçao de Campos Nulos
    if ((txtTelaOSPesquisaIdCli.getText().isEmpty()) || (txtTelaOsTec.getText().isEmpty()) || (txtBuscarProdutId.getText().isEmpty()) || (txtTelaOsDefeito.getText().isEmpty()) || (txtTelaOsServico.getText().isEmpty()) || (txtValorOS.getText().isEmpty()) ) {
            JOptionPane.showMessageDialog(null, "Preencha os Campos Obrigatórios");
    }else{
        
        emitir.setTipoOs(tipo);
        emitir.setStatus((String)jcbTelaOsStatus.getSelectedItem());
        emitir.setIdCliente(Integer.parseInt(txtTelaOSPesquisaIdCli.getText()));
        emitir.setEqpDesc(txtDescricao.getText());
        emitir.setEqpFabric(txtFabricante.getText());
        emitir.setEqpModelo(txtModelo.getText());
        emitir.setEqpSerie(txtNserie.getText());
        emitir.setDefeito(txtTelaOsDefeito.getText());
        emitir.setServico(txtTelaOsServico.getText());
        emitir.setIdProduto(Integer.parseInt(txtBuscarProdutId.getText()));
        emitir.setIdFuncionario(Integer.parseInt(txtTelaOsTec.getText()));
        emitir.setDescricao(jtxDescOS.getText());
        emitir.setValor(Float.parseFloat(txtValorOS.getText()));
        emitir.setGarantia((String)jcbTelaOsGarantia.getSelectedItem());
        emitir.setData(txtTelaOsData.getText());
                
        //criando o objeto
        OsDAO dao = new OsDAO();
        try {
            dao.emitirOs(emitir);
            txtBuscarProdutId.setText("");
            txtDescricao.setText("");
            txtFabricante.setText("");
            txtModelo.setText("");
            txtNserie.setText("");
            txtTelaOSNumOS.setText("");
            txtTelaOSPesquisaIdCli.setText("");
            txtTelaOsData.setText("");
            txtTelaOsDefeito.setText("");
            txtTelaOsPesquisaNomeCliente.setText("");
            txtTelaOsServico.setText("");
            txtTelaOsTec.setText("");
            txtValorOS.setText("");
            jtxDescOS.setText("");
                        
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
        
        }  
        
    }//GEN-LAST:event_btnSalvarActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        // TODO add your handling code here:
        // Restriçao de Botoes
        btnSalvar.setEnabled(true);
        btnEditar.setEnabled(false);
        btnDelete.setEnabled(false);
        btnCancelar.setEnabled(false);
        
        Os p = new Os();
        OsDAO pdao = new OsDAO();
                    
        p.setTipoOs(tipo);
        p.setStatus((String) jcbTelaOsStatus.getSelectedItem());
        p.setIdCliente(Integer.parseInt(txtTelaOSPesquisaIdCli.getText()));
        p.setEqpDesc(txtDescricao.getText());
        p.setEqpFabric(txtFabricante.getText());
        p.setEqpModelo(txtModelo.getText());
        p.setEqpSerie(txtNserie.getText());
        p.setDefeito(txtTelaOsDefeito.getText());
        p.setServico(txtTelaOsServico.getText());
        p.setIdProduto(Integer.parseInt(txtBuscarProdutId.getText()));
        p.setIdFuncionario(Integer.parseInt(txtTelaOsTec.getText()));
        p.setDescricao(jtxDescOS.getText());
        p.setValor(Float.parseFloat(txtValorOS.getText()));
        p.setGarantia((String) jcbTelaOsGarantia.getSelectedItem());
        p.setData(txtTelaOsData.getText());
        p.setId(Integer.parseInt(txtTelaOSNumOS.getText()));
                
        try {
            pdao.updateOs(p);
            //JOptionPane.showMessageDialog(null, "Editado com sucesso!");
            // Apaga Campos ao Editar
            txtBuscarProdutId.setText("");
            txtDescricao.setText("");
            txtFabricante.setText("");
            txtModelo.setText("");
            txtNserie.setText("");
            txtTelaOSNumOS.setText("");
            txtTelaOSPesquisaIdCli.setText("");
            txtTelaOsData.setText("");
            txtTelaOsDefeito.setText("");
            txtTelaOsPesquisaNomeCliente.setText("");
            txtTelaOsServico.setText("");
            txtTelaOsTec.setText("");
            txtValorOS.setText("");
            jtxDescOS.setText("");
                        
        } catch (SQLException | HeadlessException e) {
            JOptionPane.showMessageDialog(null, "Erro ao editar!" +e);
        }
                 
    }//GEN-LAST:event_btnEditarActionPerformed

