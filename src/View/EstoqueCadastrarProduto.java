    private void btnCadProSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadProSalvarActionPerformed
        // TODO add your handling code here:
       
        Produto cadastrar = new Produto();

        cadastrar.setDescricao(txtEstoqueCadPro_nome.getText());
        cadastrar.setPrecoCompra(Float.parseFloat(txtEstoqueCadPro_preCompra.getText()));
        cadastrar.setPrecoVenda(Float.parseFloat(txtEstoqueCadPro_preVenda.getText()));
        cadastrar.setFornecedor(txtEstoqueCadPro_fornecedor.getText());
        cadastrar.setQuantidade(Integer.parseInt(txtCadPro_quantidade.getText()));

        ProdutosDAO dao = new ProdutosDAO();

        try {
            dao.cadastrarProduto(cadastrar);
            JOptionPane.showMessageDialog(null, "Cadastrado com sucesso!");
            txtEstoqueCadPro_nome.setText("");
            txtEstoqueCadPro_preCompra.setText("");
            txtEstoqueCadPro_preVenda.setText("");
            txtEstoqueCadPro_fornecedor.setText("");
            txtCadPro_quantidade.setText("");

            //limpar e atualizar tabela
            while (jTControleProdutos.getRowCount() > 0) {
                DefaultTableModel dm = (DefaultTableModel) jTControleProdutos.getModel();
                dm.getDataVector().removeAllElements();
            }
            DefaultTableModel modelo = (DefaultTableModel) jTControleProdutos.getModel();

            ProdutosDAO pdao = new ProdutosDAO();
            try {
                
                for (Produto p : pdao.read()) {

                    modelo.addRow(new Object[]{
                        p.getIdproduto(),
                        p.getDescricao(),
                        p.getPrecoCompra(),
                        p.getPrecoVenda(),
                        p.getFornecedor(),
                        p.getQuantidade()
                    });

                }
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, ex);
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Erro ao cadastrar!" + ex);

        }


    }//GEN-LAST:event_btnCadProSalvarActionPerformed

