 public void readJtable (){
        DefaultTableModel modelo = (DefaultTableModel)jTControleProdutos.getModel();
        ProdutosDAO pdao = new ProdutosDAO();
        try {
            for (Produto p: pdao.read()) {
                
                modelo.addRow(new Object[]{
                    p.getIdproduto(),
                    p.getDescricao(),
                    p.getPrecoCompra(),
                    p.getPrecoVenda(),
                    p.getFornecedor(),
                    p.getQuantidade()
                });
                
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
    }
 


