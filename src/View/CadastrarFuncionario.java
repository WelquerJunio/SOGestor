    public void readJtableForDesc (String desc){
        DefaultTableModel modelo = (DefaultTableModel)jTPesquisaUsuario.getModel();
        FuncionarioDAO udao = new FuncionarioDAO();
        try {
            for (Funcionario p: udao.readForDesc(desc)) {
                
                modelo.addRow(new Object[]{
                    p.getIdfuncionario(),
                    p.getNome(),
                    p.getCpf(),
                    p.getCelular(),
                    p.getEmail()
                    
                    
                });
                
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
    }
    
    
    

