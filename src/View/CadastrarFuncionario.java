    private void btnCadFuncionarioSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadFuncionarioSalvarActionPerformed

        //instru��es para cadastro
        //Validaçao de Campos Nulos
        if ((txtCadFuncionarioCPF.getText().isEmpty()) || (txtCadFuncionarioEmail.getText().isEmpty()) || (txtCadFuncionarioNome.getText().isEmpty()) || (txtCadFuncionarioCelular.getText().isEmpty()) ) {
            JOptionPane.showMessageDialog(null, "Preencha os Campos Obrigatórios");
        
        // Validacao de Email
        }else if (!(Pattern.matches(".+@.+\\.[a-z]+", txtCadFuncionarioEmail.getText()) )) {
            JOptionPane.showMessageDialog(null, "Informe um Email Valido", "Error", JOptionPane.ERROR_MESSAGE);
        } else 
        
        {

            Funcionario cadastrar = new Funcionario();
            cadastrar.setNome(txtCadFuncionarioNome.getText());
            cadastrar.setRg(txtCadFuncionarioRG.getText());
            cadastrar.setCpf(txtCadFuncionarioCPF.getText());
            cadastrar.setTelefone(txtCadFuncionarioTelefone.getText());
            cadastrar.setCelular(txtCadFuncionarioCelular.getText());
            cadastrar.setEmail(txtCadFuncionarioEmail.getText());
            cadastrar.setDataCadastro(txtCadFuncionarioDTCadastro.getText());
            cadastrar.setCargo((String) jbCadFuncionarioCargo.getSelectedItem());
            cadastrar.setFormacao(txtCadFuncionarioFormacao.getText());
            cadastrar.setSalario(txtCadFuncionarioSalario.getText());
            cadastrar.setCtps(txtCadFuncionarioCtps.getText());
            cadastrar.setNCarteTraba(txtCadFuncionarioNCarteTraba.getText());
            cadastrar.setEndereco(txtCadFuncionarioEndereco.getText());
            cadastrar.setBairro(txtCadFuncionarioBairro.getText());
            cadastrar.setCidade(txtCadFuncionarioCidade.getText());
            cadastrar.setEstado((String) jbCadFuncionarioEstado.getSelectedItem());
            cadastrar.setCep(txtCadFuncionarioCep.getText());

            FuncionarioDAO dao = new FuncionarioDAO();

            try {
                dao.cadastrarFuncionario(cadastrar);
                JOptionPane.showMessageDialog(null, "Cadastrado com sucesso!");
                
                //apagar campos ao salvar
                txtCadFuncionarioNome.setText("");
                txtCadFuncionarioCPF.setText("");
                txtCadFuncionarioRG.setText("");
                txtCadFuncionarioCelular.setText("");
                txtCadFuncionarioTelefone.setText("");
                txtCadFuncionarioEmail.setText("");
                //txtCadFuncionarioDTCadastro.setText("");
                jbCadFuncionarioCargo.setSelectedItem("");
                txtCadFuncionarioFormacao.setText("");
                txtCadFuncionarioSalario.setText("");
                txtCadFuncionarioCtps.setText("");
                txtCadFuncionarioNCarteTraba.setText("");
                txtCadFuncionarioEndereco.setText("");
                txtCadFuncionarioBairro.setText("");
                txtCadFuncionarioCidade.setText("");
                jbCadFuncionarioEstado.setSelectedItem("");
                txtCadFuncionarioCep.setText("");
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, "Erro ao cadastrar!" + ex);

            }
        }


    }//GEN-LAST:event_btnCadFuncionarioSalvarActionPerformed

