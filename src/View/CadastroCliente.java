    public void readJtableForDesc(String desc) {
        DefaultTableModel modelo = (DefaultTableModel) jTPesquisaUsuario.getModel();
        ClientesDAO udao = new ClientesDAO();
        try {
            for (Clientes p : udao.readForDesc(desc)) {

                modelo.addRow(new Object[]{
                    p.getIdClientes(),
                    p.getNome(),
                    p.getCpfCnpj(),
                    p.getTelefone(),
                    //p.getCelular(),
                    p.getEmail(), //p.getEndereco(),
                // p.getSetor(),
                //p.getCidade(),
                // p.getCep(),
                //p.getEstado(),
                //p.getDatacadastro()
                });

            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
    }

