    private void jbCadClienteCNPJActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbCadClienteCNPJActionPerformed
        // Mensagem de erro, Pegar Mascara CPF ou CNPJ:
        int selCnpj = jbCadClienteCNPJ.getSelectedIndex();
        txtCadClienteCNPJ.setValue(null);
        switch (selCnpj) {
            case 0:
                JOptionPane.showMessageDialog(rootPane, "Selecione um Tipo");
                break;
            case 1:
                txtCadClienteCNPJ.setFormatterFactory(CPF.getFormat());
                break;
            case 2:
                txtCadClienteCNPJ.setFormatterFactory(CNPJ.getFormat());
                break;
        }
    }//GEN-LAST:event_jbCadClienteCNPJActionPerformed

